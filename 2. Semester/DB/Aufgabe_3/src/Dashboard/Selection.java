    public void printRolleID(){
        int rolleid = Verbindung.getRolleID();
        switch(rolleid){
            case 1 : jLabel1.setText(" Ihr Name:"+Verbindung.benutzer2()+" | Ihre Rolle: DB-Admin ");
                    break;
            case 2 : jLabel1.setText(" Ihr Name:"+Verbindung.benutzer2()+" | Ihre Rolle: Content-Admin ");
                    break;
            case 3 : jLabel1.setText(" Ihr Name:"+Verbindung.benutzer2()+" | Ihre Rolle: Birdwatcher ");
                    break;
            default: jLabel1.setText(" Ihr Name:"+Verbindung.benutzer2()+" | Keine Rolle ");
                    break;
        }
    }

